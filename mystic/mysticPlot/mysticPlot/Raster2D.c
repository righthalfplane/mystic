static int doMouseRotateDown(IconPtr myIcon)
{
	uPoint current,old;
	int dx,dy;
	long CurrentFrame,Show;
	int xsize,ysize;
	long tiltsteps;
	long rotationsteps;
	long row,column;
	ras2dPtr nw;
	uRect Box;

	if(!myIcon)return 1;
	nw=(ras2dPtr)myIcon->DWindow;
	if(!nw)return 1;

	if(nw->ImageCount <= 0)return 0;

	CurrentFrame=nw->CurrentFrame;

	uPointer(&old,myIcon);

	uGetPortBounds(myIcon,&Box);

	xsize=Box.xsize;
	ysize=Box.ysize-(ScrollOffSet+myIcon->mMenuOffSet+DataSpace);
	tiltsteps=nw->tiltsteps;
	rotationsteps=nw->rotationsteps;
	if(rotationsteps < 0)rotationsteps=1;
	if(nw->ImageCount != tiltsteps*rotationsteps){
	    sprintf(WarningBuff,"Error ImageCount %ld != tiltsteps*rotationsteps %ld\n",
		       (long)nw->ImageCount,(long)tiltsteps*rotationsteps);
	    WarningBatch(WarningBuff);
	    return 1;
	}
	while(uStillDown(myIcon)){
	uPointer(&current,myIcon);
		    row=CurrentFrame/rotationsteps;
		    column=CurrentFrame-row*rotationsteps;
		    if(current.x < 15){
		       if(++column >= rotationsteps)column=0;
		    }else if(current.x > xsize-31){
		       if(--column < 0)column=rotationsteps-1;
		    } else if(current.y < 40){
		       if(++row >= tiltsteps)row = tiltsteps-1;
		    }else if(current.y > ysize-31){
		       if(--row < 0)row = 0;
		    }else{
			dx=current.x-old.x;
			dy=current.y-old.y;
		        if(abs(dx) < 5 &&  abs(dy) < 5)continue;
		        if(dx < -5){
		           if(++column >= rotationsteps)column=0;
		        }else if(dx > 5){
		           if(--column < 0)column=rotationsteps-1;
		        }
		        if(dy < -5){
		           if(++row >= tiltsteps)row = tiltsteps-1;
		        }else if(dy > 5){
		           if(--row < 0)row = 0;
		        }
		    }
		    CurrentFrame=column+row*rotationsteps;
		    old=current;
		    Show = CurrentFrame;
		    if(Show >= 0){
		       Show %= nw->ImageCount;
		    }else{
		       Show = -Show;
		       Show %= nw->ImageCount;
		       if(Show)Show = nw->ImageCount - Show;
		    }
		    nw->CurrentFrame=Show;
		    if(nw->CurrentFrame >= nw->ImageCount){
		        nw->CurrentFrame=0;
		    }else if(nw->CurrentFrame < 0){
		       nw->CurrentFrame=nw->ImageCount-1;
		    }

			SetFrame(myIcon);
	    	drawIt(myIcon);
	    	
		    if(myIcon->Synchronize)Synchronize(myIcon,nw->CurrentFrame);
		    CurrentFrame=nw->CurrentFrame;
			FlushCurrentPort();
	}

	return 0;
}
