static int uDrawSync(IconPtr myIcon,struct SyncDrawInfo *DSync)
{
	struct SyncStruct m;
	uRect *DRect;
	uPoint pt;
	
	if(!myIcon || !DSync)return 1;
	DRect=&DSync->DRect;
	if(DRect->xsize < 2 || DRect->ysize < 2)return 0;
	
	zerol((char *)&m,sizeof(struct SyncStruct));
	
	pt.x=DRect->x;
	pt.y=DRect->y;
	
	IntLocC(myIcon,&m.xminData,&m.ymaxData,pt.x,pt.y);
	
	pt.x += DRect->xsize;
	pt.y += DRect->ysize;
	
	IntLocC(myIcon,&m.xmaxData,&m.yminData,pt.x,pt.y);
	
	IntLocC(myIcon,&m.xCurrent,&m.yCurrent,DSync->current.x,DSync->current.y);
	
	IntLocC(myIcon,&m.xStart,&m.yStart,DSync->start.x,DSync->start.y);
	
	SendMessageToAll(myIcon,FALSE,MessageType_SET_SYNC_REGION,&m);
	
	return 0;
}
