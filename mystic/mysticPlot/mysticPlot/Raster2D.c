static int pioGetValue(IconPtr myIcon,double x,double y,double *v)
{
    struct SetFrameData  *sd;
   	struct LIMITS *limits;
    double *d,dd;
	ras2dPtr s;
	long i,j;

	if(!myIcon || !v)return 0;
	s=(ras2dPtr)myIcon->DWindow;
	if(!s)return 0;
	sd = &s->sd;
	d=(double *)sd->data;
	if(!d)return 0;
	if(s->raster.plotType == FRAME_DATA_RGB)return 0;
	
	limits=&sd->limits;
	
	if(x < limits->xmin || x > limits->xmax)return 0;
	if(y < limits->ymin || y > limits->ymax)return 0;
	
	i=(long)((x-limits->xmin)*((double)sd->xsize)/(limits->xmax-limits->xmin));
	if(i < 0){
	    i=0;
	}else if(i >= sd->xsize){
	    i=sd->xsize-1;
	}
	
	j=(long)((y-limits->ymin)*((double)sd->ysize)/(limits->ymax-limits->ymin));
	if(j < 0){
	    j=0;
	}else if(j >= sd->ysize){
	    j=sd->ysize-1;
	}
	
	j=sd->ysize-1-j;
	
	dd=d[i+j*sd->xsize];
	
	if(dd == FLOAT_NOT_SET)return 0;
	
	*v = dd;
		
	return 1;
		
}
