static int push(uRect *oldR,IconPtr myIcon)
{
	struct FileInfo2 *Files;
	long xsize,ysize;
	double xs,ys;
	double xoff,yoff;
	ras2dPtr t;
	int x,y;
	
	if(!oldR)return 1;
	if(!myIcon)return 1;
	t=(ras2dPtr)myIcon->DWindow;
	if(!t)return 1;
	Files=myIcon->Files;
	if(!Files)return 1;
		
	xsize=oldR->xsize;
	ysize=oldR->ysize;

	if(xsize <= 2 || ysize <= 2)return 0;

	x=(int)(uGetControlValue(myIcon->HControl)+oldR->x-t->showXspace);
	y=(int)(uGetControlValue(myIcon->VControl)+oldR->y-myIcon->mMenuOffSet);
	
	xoff=(double)x/(t->raster.xScale*(double)t->raster.xsize);
	yoff=(double)y/(t->raster.yScale*(double)t->raster.ysize);
	
	xs=(double)xsize/(double)t->raster.box.xsize;
	ys=(double)ysize/(double)t->raster.box.ysize;
	
	if(xs > ys){
	   t->raster.xScale /= xs;
	   t->raster.yScale /= xs;
	}else{
	   t->raster.xScale /= ys;
	   t->raster.yScale /= ys;
	}
	
	
	x=(int)(xoff*t->raster.xScale*(double)t->raster.xsize);
	y=(int)(yoff*t->raster.yScale*(double)t->raster.ysize);
				
	
	tMoveControls(myIcon);

	uSetControlValue(myIcon->HControl,x);
	uSetControlValue(myIcon->VControl,y);
	
 	InvalRectMyWindow(myIcon);
 	    
	return 0;
}
