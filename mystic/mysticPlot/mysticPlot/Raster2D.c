static int drawImageSpeed(IconPtr myIcon,long CurrentFrame,uRect *DRect)
{
	struct FileInfo2 *Files;
    struct SetFrameData *s;
    uRect LRect;
	ras2dPtr t;
	unsigned char *buffRead;
	uRect SRect;
	int xmax,ymax;

	if(!DRect)return 1;
	if(!myIcon)return 1;
	t=(ras2dPtr)myIcon->DWindow;
	if(!t)return 1;
	Files=myIcon->Files;
	if(!Files)return 1;
	
	if(FilesSetFrame(CurrentFrame,Files))return 1;

	t->drawType=1;

	s = &Files->SetData;
		
	SRect=t->raster.box;

	LRect = *DRect;

	xmax=(int)(t->raster.xScale*(double)s->xsize);
	if(LRect.xsize > xmax)LRect.xsize=xmax;
	ymax=(int)(t->raster.yScale*(double)s->ysize);
	if(LRect.ysize > ymax)LRect.ysize=ymax;
	
	if((t->raster.xScale != 1.0) || (t->raster.yScale != 1.0)){		
		double oxscale,oyscale;
		oxscale=1.0/t->raster.xScale;
		SRect.x=(int)(oxscale*(double)SRect.x);
		SRect.xsize=(int)(oxscale*(double)SRect.xsize);
		oyscale=1.0/t->raster.yScale;
		SRect.y=(int)(oyscale*(double)SRect.y);
		SRect.ysize=(int)(oyscale*(double)SRect.ysize);
    }
	if(SRect.x < 0)SRect.x=0;
	if(SRect.y < 0)SRect.y=0;    
	if(SRect.y+SRect.ysize > s->ysize)SRect.ysize = (int)(s->ysize-SRect.y);
	if(SRect.x+SRect.xsize > s->xsize)SRect.xsize = (int)(s->xsize-SRect.x);
	
	

	if(SetBuffers(s->xsize*s->ysize*3+512L))return 1;
	    
	if(s->type == SDS2D_FILES){
		struct SetFrameData sd;
		
	    zerol((char *)&sd,sizeof(struct SetFrameData));
	    
	    sd.xsize=s->xsize;
	    sd.ysize=s->ysize;
	    sd.data=s->data;
	    
	    sd.pd=t->pd;
	    
		buffRead=(unsigned char *)xg.buffin;
		
		if(sd.pd.UsePlotScales){
			struct LIMITS *limits;
			FileGetLimits(&limits,CurrentFrame,Files);
			if(limits && limits->iGot){
				if(limits->dolog){
				   sd.pd.sType = 1;
				}else{
				   sd.pd.sType = 2;
				}

				if(limits->vmin == limits->vmax){
				   sd.pd.sType = 0;
				}

				sd.pd.sPmin=limits->vmin;
				sd.pd.sPmax=limits->vmax;									
			}
		}
		if(sd.pd.sType == 0){
			register double pmin,pmax,d;
		    register double *data;
		    long nn,length;
		    length=sd.xsize*sd.ysize;
		    data=(double *)sd.data;
		    pmin =  data[0];
		    pmax = pmin;
		    for(nn=0;nn<length;++nn){
		        d=data[nn];
		        if(d < pmin){
		            pmin=d;
		        }else if(d > pmax){
		            pmax=d;
		        }
		    }
		    sd.pd.dmin=pmin;
		    sd.pd.dmax=pmax;
		
		}
		
	    FrameDataToRaster(&sd,buffRead);	  
	      	    
StartDraw:
		uScreenWrite(s->xsize,s->ysize,(unsigned char *)buffRead,(unsigned char *)xg.buffout,ScreenDataType8,&SRect,&LRect,myIcon);   
	}else if(s->type == HDF_FILES){
		buffRead=(unsigned char *)s->data;
		goto StartDraw;
	}else if(s->type == PICT_FILES){
		uScreenWrite(s->xsize,s->ysize,(unsigned char *)&s->data,(unsigned char *)xg.buffout,ScreenDataTypePict,&SRect,&LRect,myIcon);   
	}else{
		uScreenWrite(s->xsize,s->ysize,(unsigned char *)s->data,(unsigned char *)xg.buffout,ScreenDataType24,&SRect,&LRect,myIcon);   
	}

	return 0;
}

