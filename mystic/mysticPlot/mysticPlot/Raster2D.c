static int PIO2dGetMessage(IconPtr myIcon,long MessageType,void *MessageData)
{
	struct PlotRangeRange *d;
	struct PlotRangeData *pio;
	struct SetFrameData  *sd;
	struct SetFrameData   sd2;
	struct Message1000 *m;
	struct SyncStruct *sync;
	struct linedata *li;
	struct areadata *ai;
	struct LIMITS *limits;
	struct LIMITS *limitl;
	char windowName[256];
	uRect oldR;
	char buff[256];
	uPort save;
	ras2dPtr s;
	long n;
	int ret;
	int setSDS2D;
    double dx,dy,dd;
    long dxi,dyi;
    int iflag;

	if(!myIcon)return 1;
	s=(ras2dPtr)myIcon->DWindow;
	if(!s)return 1;
	pio = &s->range;
	m=(struct Message1000 *)MessageData;
	
	
	setSDS2D=FALSE;
	if(MessageType == MessageType_SET_REGION_DATA){
	    if(!myIcon->Files){
        	myIcon->Files=FilesSDS2D();
        	if(!myIcon->Files)return 1;        	
			sprintf(buff,"Raster_%03d",Count++);			
			mstrncat(buff,".b",255);        	
			if(FileManagerAddToList(myIcon->Files,buff))return 1;
		    mstrncpy(s->BatchName,buff,255);
			setSDS2D=TRUE;
	    }	
	}
	
	if(!myIcon->Files)return 1;

	ret=1;
	uGetPort(&save);
	SetPortMyWindow(myIcon);
	
	switch(MessageType){
	
	case MessageType_LIMITS:
	    if(!pio->receiveRange)goto OK;
	    if(!m)goto ErrorOut;	    
	    if(!s->BatchName[0])goto OK;
	    
		if(SendMessageByName(s->BatchName,MessageType_GET_INFORMATION,&s->sd)){
			goto OK;
		}	    
		
		s->range=s->sd.range;
		
		d=&s->sd.rangeData;
			
		if(d->xsize <= 0 || m->ixmax <= 0)goto OK;
		
	    pio->MaintainAspectRatio=m->MaintainAspectRatio;
	    	    			
	    iflag=0;
	    if(pio->ixmax != m->ixmax || pio->iymax != m->iymax){
		    pio->ixmax=m->ixmax;
		    pio->iymax=m->iymax;	    
	        iflag=1;
	    }
	    
	    dx=m->xmaxData-m->xminData;
	    dy=m->ymaxData-m->yminData;
	    if(dy > dx){
	        dd=dy;
	    }else{
	    	dd=dx;
	    }
	    
	    dd *= 1e-4;
	    
	    if(fabs(pio->xminData-m->xminData) > dd)iflag=1;
	    if(fabs(pio->xmaxData-m->xmaxData) > dd)iflag=1;
	    if(fabs(pio->yminData-m->yminData) > dd)iflag=1;
	    if(fabs(pio->ymaxData-m->ymaxData) > dd)iflag=1;
	    
	    if(iflag == 0)goto OK;
	    
		if(pushStack(myIcon)){
		    goto OK;
		}
	
	    
	    dxi=m->ixmaxData-m->ixminData;
	    dyi=m->iymaxData-m->iyminData;
	    
	    pio->ixmax=dxi;
	    pio->iymax=dyi;

		if(ScreenSetSize(pio->ixmax,pio->iymax,myIcon))goto ErrorOut;
	    
	    pio->xminSet=1;
	    pio->xmaxSet=1;
	    pio->yminSet=1;
	    pio->ymaxSet=1;

		uSetControlValue(myIcon->HControl,0);
		uSetControlValue(myIcon->VControl,0);
		
		s->raster.xsize=d->xsize;
		s->raster.ysize=d->ysize;
		s->raster.xScale=(d->xmax-d->xmin)*(double)dxi/(dx*(double)d->xsize);
		s->raster.yScale=(d->ymax-d->ymin)*(double)dyi/(dy*(double)d->ysize);
		
		s->raster.box.xsize=oldR.xsize=(int)pio->ixmax;
		s->raster.box.ysize=oldR.ysize=(int)pio->iymax;
		
		oldR.x=(int)((m->xminData-d->xmin)*(double)dxi/dx);
		oldR.y=myIcon->mMenuOffSet+(int)((d->ymax-m->ymaxData)*(double)dyi/dy);
		
    	uSizeWindow(myIcon,(int)(pio->ixmax+ScrollOffSet),(int)(pio->iymax+myIcon->mMenuOffSet+ScrollOffSet+myIcon->CursorSpace));	
		
    	FlushCurrentPort();
    	
		push(&oldR,myIcon);

		break;
	case MessageType_SET_SYNC_REGION:
	    if(s->Tool != SyncRegions)goto OK;
		sync=(struct SyncStruct *)MessageData;
		if(!sync)goto ErrorOut;
		s->syncOld=s->sync;
	    s->sync = *sync;
	    doSyncFrame(myIcon);
	    break;
	case MessageType_SET_FRAME_NUMBER:
	    if(!myIcon->Synchronize)goto OK;
	    s->CurrentFrame=m->CurrentFrame;
	    if(myIcon->uDrawAnimate){
	        (*myIcon->uDrawAnimate)(myIcon,s->CurrentFrame);
	    }
	    goto OK;
	case MessageType_SET_PALETTE:
		for(n=0;n<256;++n){
		    myIcon->red[n]=m->red[n];
		    myIcon->green[n]=m->green[n];
		    myIcon->blue[n]=m->blue[n];
		}
		break;
	case MessageType_GET_FRAME_COUNT:
	    if(myIcon->Files){
		    if(myIcon->Files->ImageCount > m->FrameCount)m->FrameCount=myIcon->Files->ImageCount;
		}
		goto OK;
	case MessageType_GET_LINE_DATA:
	    li=(struct linedata *)MessageData;
	    ret = SendMessageByName(s->BatchName,MessageType_GET_LINE_DATA,li);
	    goto ErrorOut;
	case MessageType_GET_AREA_DATA:
	    ai=(struct areadata *)MessageData;
		ret=SendMessageByName(s->BatchName,MessageType_GET_AREA_DATA,ai);
		if(!ret)myIcon->limits=myIcon->Files->limits;
	    goto ErrorOut;	    
	case MessageType_GET_REGION_DATA:
	    sd=(struct SetFrameData  *)MessageData;
	    if(myIcon->pGetData){
	    	ret = (*myIcon->pGetData)(myIcon,sd->CurrentFrame,sd);
	    }
	    goto ErrorOut;
	case MessageType_SET_AREA_RANGE:
	    ai=(struct areadata *)MessageData;
	    ret = SendMessageByName(s->BatchName,MessageType_SET_AREA_RANGE,ai);
	    goto ErrorOut;
	case MessageType_SET_REGION_DATA:
	    sd=(struct SetFrameData  *)MessageData;
	    if(myIcon->pPutData){
	    	ret = (*myIcon->pPutData)(myIcon,sd->CurrentFrame,sd);	
	    	if(ret){
	    	   ret = -1;
	    	}    	
			if(!ret && setSDS2D){
				doSDS2DStart(myIcon->Files);
				if(SendMessageByName(s->BatchName,MessageType_GET_INFORMATION,&sd2)){
					goto ErrorOut;
				}
				
				s->range  = sd2.range;
				s->vector = sd2.vector;
				s->pd=sd2.pd;
				if(s->sd.pioName[0]){
		    		mstrncpy(s->pioName,s->sd.pioName,255);
		    		s->pioIndex=s->sd.pioIndex;			    
				}					
				s->raster.xsize=s->range.ixmax;
				s->raster.ysize=s->range.iymax;
				s->ImageCount=myIcon->Files->ImageCount;	    	
				doFrameImage(myIcon);
			}
			
			s->ImageCount=myIcon->Files->ImageCount;	    	
	    }
	    goto ErrorOut;
	case MessageType_GET_LIMITS_DATA:
	    limitl=(struct LIMITS *)MessageData;
	    if(!limitl)goto ErrorOut;
	    limits=(struct LIMITS *)NULL;
		FileGetLimits(&limits,limitl->CurrentFrame,myIcon->Files);
		if(limits && limits->iGot){
		    limits->CurrentFrame=limitl->CurrentFrame;
		    *limitl = *limits;
		}
		break;
	case MessageType_GET_FILE_LIST:
	    {
		    int isFound;
		    
			if(!myIcon->Files)goto OK;
			
		    isFound=FALSE;
			for(n=0;n<m->FileCount;++n){
			    if(myIcon->Files == m->FilesList[n]){isFound=TRUE;break;}
			}
			if(isFound == FALSE && (m->FileCount < m->FileCountMax-1)){
			    m->FilesList[m->FileCount++]=myIcon->Files;
			}
		}
		goto OK;
	case MessageType_GET_FILE_OWNER:
		for(n=0;n<m->FileCount;++n){
		    if(myIcon->Files == m->FilesList[n])m->FileOwner[n] = TRUE;
		}
		goto OK;
	case MessageType_PRINT_FILE_INFORMATION:
		GetWindowName(myIcon,windowName,0);
	    sprintf(WarningBuff,"Window \'%s\'\n%c",windowName,0);
	    WarningBatch(WarningBuff);
	    SendMessageByName(s->BatchName,MessageType_PRINT_FILE_INFORMATION,m);
		goto OK;
	}
	InvalRectMyWindow(myIcon);
	drawIt(myIcon);
OK:
	ret = 0;
ErrorOut:
	uSetPort(&save);
	return ret;
}


